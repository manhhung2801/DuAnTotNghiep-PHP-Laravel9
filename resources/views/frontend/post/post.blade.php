                                    <p>Sản phẩm đầu tiên lên kệ với camera thò thụt là Vivo NEX vào năm 2018, mở đường cho nhiều sản phẩm khác ra mắt sau đó vào năm 2019.</p>

                                    <p>Những sản phẩm như Reno 10X Zoom của Oppo mang đến thiết kế nổi bật, nhưng chính dòng Mi 9T của Xiaomi và OnePlus 7 Pro/7T Pro mới khiến thiết kế camera selfie thò thụt trở thành xu hướng chủ đạo.</p>

                                    <p>Tuy nhiên, vì một số lý do, xu hướng này bất ngờ đi xuống dù chỉ mới ra mắt không lâu. Các thiết kế thò thụt giảm dần vào năm 2020 và biến mất hoàn toàn vào năm 2021.</p>

                                    <p>Các thiết bị gần đây nhất có camera thò thụt là Lenovo Legion Duel 2, tập trung vào chơi game và Asus ZenFone 8 Flip. Cả hai thiết bị đều có lượng bán hạn chế, điều này cho thấy nhu cầu thật sự không nhiều.</p>

                                    <h2><b>Cái chết của camera thò thụt có phải tại Apple?</b></h2>

                                    <p>Vì sao xu hướng camera được coi là tương lai như vậy lại chết yểu nhanh chóng? Phải chăng các ông lớn như Apple và Samsung chưa bao giờ chấp nhận thiết kế này chính là lý do?</p>

                                    <p>Trên thực tế, nguyên nhân đến từ các vấn đề kỹ thuật. Việc hướng đến những chiếc điện thoại mỏng và nhẹ hơn là một trong những lý do giúp camera selfie thò thụt trở nên phổ biến. Tuy nhiên, các mô-đun này không
                                        phục vụ mục đích như các nhà sản xuất mong đợi.</p>

                                    <p>Thay vào đó, chúng lại gia tăng thêm trọng lượng và độ dày tổng thể của điện thoại do có nhiều bộ phận phức tạp cần thiết để hoạt động đúng cách.</p>

                                    <p>Bên cạnh sự phức tạp, máy ảnh có thể thu vào cũng đồng nghĩa là sẽ có thêm một lỗ để hút hơi ẩm vào điện thoại. Chính vì lẽ đó, không có thoại nào trong số này hỗ trợ xếp hạng chống nước IP6X.</p>

                                    <p>Mặc dù các nhà sản xuất luôn tuyên bố rằng camera selfie thò thụt đã được thử nghiệm rộng rãi để đảm bảo chúng bền bỉ theo năm tháng, nhưng lẽ thường các bộ phận hay hoạt động sẽ dễ bị hao mòn hơn so với các
                                        bộ phận nằm yên.</p>

                                    <p>&nbsp;</p>

