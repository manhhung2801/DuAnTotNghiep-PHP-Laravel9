                                    <h2 class="fs-4"><b>Cái chết của camera thò thụt</b></h2>
                                    <p>Nỗi ám ảnh về màn hình không viền trên điện thoại thông minh đã được thúc đẩy bởi Mi MIX của Xiaomi vào năm 2016 khi tỷ lệ màn hình trên thân máy của thiết bị này lên tới 91,3%, khiến công chúng trầm trồ.</p>
                                    <p>Để đạt được thiết kế này, Xiaomi đã sử dụng một giải pháp độc đáo là đặt camera ở viền dưới của điện thoại.</p>
                                    <p>Những hãng sản xuất khác như Samsung đã thực hiện các giải pháp tăng tỷ lệ màn hình ít phức tạp hơn bằng cách thu nhỏ viền trên Galaxy S8 và S9. iPhone X và OnePlus 6 của Apple thì có phần tai thỏ, nhưng vẫn
                                        bị đánh giá là thiếu tính thẩm mỹ.</p>
                                    <p>Không lâu sau, cuộc cạnh tranh chuyển sang viền màn hình dạng giọt nước trước khi chốt lại ở viền đục lỗ phổ biến hiện nay. Tuy nhiên, trong vài năm trước đó, thứ mà công chúng bàn tán nhiều nhất lại là dạng
                                        camera selfie thò thụt (pop up).</p>
                                    <p>Mặc dù đã nỗ lực hết sức, không có thiết kế nào giành chiến thắng trong cuộc đua tăng tỷ lệ màn hình bằng thiết kế camera selfie dạng thò thụt. Hình thái này cho phép tỷ lệ màn hình trên thân máy ở mức cao mà
                                        không có nhược điểm của tai thỏ, đục lỗ hay thậm chí là viền dày.</p>
                                    <p>Thiết kế cũng mang đến cho điện thoại một giao diện đầy tính tương lai đồng thời giúp người dùng không phải lo lắng về quyền riêng tư vì camera trước không phải lúc nào cũng nhìn chằm chằm vào họ.</p>
