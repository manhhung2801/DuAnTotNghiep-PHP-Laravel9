                                    <p>Đối với những người hiếm khi sử dụng camera trước, việc ẩn camera đi là điều thú vị. Họ có được chiếc điện thoại mở rộng toàn màn hình trong khi cái giá phải trả chỉ là bớt đi thứ họ không mấy khi dùng.</p>

                                    <p>Tuy nhiên, có những người chụp rất nhiều ảnh tự sướng, thực hiện nhiều cuộc gọi video và quay các video ngắn cho mạng xã hội.</p>

                                    <p>Lấy người dùng TikTok làm ví dụ. Một chiếc điện thoại có camera bật lên rồi lại thu vào mỗi khi định dùng ứng dụng có thể gây khó chịu. Tiếng ồn mà cơ chế thò thụt tạo ra cũng chẳng êm ái gì theo thời gian.</p>

                                    <p>Trong một thế giới có hơn 1 tỷ người dùng TikTok, việc có một camera selfie giấu kín trong máy không bao giờ là điều tiện dụng. Điều này cũng đúng với các ứng dụng thường sử dụng camera trước, trong số đó có
                                        Instagram, WhatsApp và thậm chí cả Telegram.</p>

                                    <p>Ngay cả các nhà sản xuất cũng nhanh chóng nhận ra rằng việc đánh đổi lấy không gian bên trong điện thoại để đặt camera là chẳng đáng. Không ngạc nhiên khi xu hướng đã kết thúc trước cả khi Apple và Samsung có
                                        ý định làm thứ gì đó hay ho.</p>

                                    <p>Nhiều người lập luận rằng chúng chỉ là một biện pháp tạm thời để thúc đẩy công nghệ máy ảnh dưới màn hình dự kiến sẽ thay thế dạng đục lỗ trong tương lai gần.</p>

                                    <p>Với máy ảnh dưới màn hình, sẽ chẳng cần phải camera thò thụt hay tai thỏ, đục lỗ gì nữa. Chỉ riêng điều này thôi cũng đủ để khiến những người quan tâm đến thiết kế toàn màn hình hào hứng.</p>

                                    <p>Tuy nhiên, camera thò thụt vẫn mang lại dư âm tích cực đối với người dùng. Trong một cuộc khảo sát gần đây, có hơn 50% người tham gia đồng ý rằng họ nhớ thiết kế camera selfie thò thụt trên điện thoại thông
                                        minh. Trong khi khoảng 27% người nói không, số còn lại cho biết họ chưa bao giờ có dạng điện thoại như vậy.</p>

                                    <p>Dù bằng cách nào, cái chết của một thứ không phù hợp sẽ mở đường cho những điều khác tốt hơn. Người dùng có thể phải kiên nhẫn hơn một chút trước khi máy ảnh dưới màn hình trở thành xu hướng.</p>
